package homework08;
/*
Используйте цикл while для решения задачи:

Распечатайте все числа от 1 до 100, которые делятся на 5 без остатка.
 */

public class Homework0803 {
    public static void main(String[] args) {
        int number = 1;

        while (number <= 100) {

                System.out.println(number);

            number++;
        }
    }
}
